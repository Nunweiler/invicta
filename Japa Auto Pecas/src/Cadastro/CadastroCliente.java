    void checarCamposObrigatorios() // Consulta se todos os campos obrigatórios foram inseridos corretamente.
    {
        String nome        = tf_nome.getText();
        String cpf         = tf_cpf.getText();
        String inscricao   = tf_inscricao_estadual.getText();
        String responsavel = tf_responsavel_compras.getText();
        
        if(nome == null || nome.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Preencha o campo Nome", "Invicta Code", JOptionPane.INFORMATION_MESSAGE);
        }
        else if(cpf == null || cpf.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Preencha o campo CPF", "Invicta Code", JOptionPane.INFORMATION_MESSAGE);
        }
        else if(inscricao == null || inscricao.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Preencha o campo Inscrição Estadual", "Invicta Code", JOptionPane.INFORMATION_MESSAGE);
        }
        else if(responsavel == null || responsavel.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Preencha o campo Responsável pelas Compras", "Invicta Code", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            gravarDados();
        }
        
    }
    
    // Lista os clientes cadastrados ao inicializar a tela  
    void listarClientes () {
        String sql = "SELECT cli_id, cli_nome, cli_cnpj, cli_nome_fantasia, cli_cidade, cli_nome_responsavel FROM cliente";
        
        DefaultTableModel modelo = (DefaultTableModel) jt_cliente.getModel();
        modelo.setNumRows(0);

        
        try 
        {
            ResultSet resultado = database.consultaTable(sql);
            while (resultado.next()){
                modelo.addRow(new Object[]{
                    resultado.getInt("cli_id"), 
                    resultado.getString("cli_nome"), 
                    resultado.getString("cli_cnpj"), 
                    resultado.getString("cli_nome_fantasia"), 
                    resultado.getString("cli_cidade"),
                    resultado.getString("cli_nome_responsavel")
                });
            }
                resultado.first();
        } 
        catch (SQLException erro) 
        {
            JOptionPane.showMessageDialog(null, "Erro ao listar a tabela " + erro);
        }
    }
    
    // Limpa tabela antes de listar todos os clientes
    void limparClienteJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jt_cliente.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
    
    void excluirCliente (int codigo){
        String sql = "DELETE FROM cliente WHERE cli_id = '"+codigo+"'";
        
        int opcao = JOptionPane.showConfirmDialog
        (null, "Deseja deletar este Cliente?", "Invicta Code", JOptionPane.INFORMATION_MESSAGE);
        if(opcao == 0)
        {
            database.updateBase(sql);
        }
    }
